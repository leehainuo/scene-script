// CodeGenerator 代码生成器
type CodeGenerator struct {
	tables []parser.TableDef
}

// NewCodeGenerator 创建代码生成器
func NewCodeGenerator(tables []parser.TableDef) *CodeGenerator {
	return &CodeGenerator{
		tables: tables,
	}
}

// GenerateTypes 生成 Types 代码
func (g *CodeGenerator) GenerateTypes() map[string]string {
	files := make(map[string]string)

	for _, table := range g.tables {
		moduleName := toModuleName(table.Name)
		content := g.generateTypesForTable(table, moduleName)
		files[moduleName+".go"] = content
	}

	return files
}

// generateTypesForTable 为单个表生成 Types
func (g *CodeGenerator) generateTypesForTable(table parser.TableDef, moduleName string) string {
	var buf bytes.Buffer

	buf.WriteString("// Code generated by scene-script-cli. DO NOT EDIT.\n\n")
	buf.WriteString("package types\n\n")

	// 检测是否需要导入 time 包
	needsTimeImport := false
	for _, col := range table.Columns {
		if col.GoType == "time.Time" {
			needsTimeImport = true
			break
		}
	}

	if needsTimeImport {
		buf.WriteString("import \"time\"\n\n")
	}

	entityName := parser.ToCamelCase(moduleName) // 使用模块名（单数）

	// 生成 Create Request
	buf.WriteString(fmt.Sprintf("// Create%sReq 创建%s请求\n", entityName, table.Comment))
	buf.WriteString(fmt.Sprintf("type Create%sReq struct {\n", entityName))
	for _, col := range table.Columns {
		if col.IsPrimaryKey || col.IsAutoIncr || col.Name == "created_at" || col.Name == "updated_at" {
			continue
		}
		buf.WriteString(fmt.Sprintf("\t%s %s", parser.ToCamelCase(col.Name), col.GoType))
		tags := fmt.Sprintf(" `json:\"%s\"", col.JSONTag)
		if col.ValidateTag != "" {
			tags += fmt.Sprintf(" validate:\"%s\"", col.ValidateTag)
		}
		tags += "`"
		buf.WriteString(tags)
		if col.Comment != "" {
			buf.WriteString(fmt.Sprintf(" // %s", col.Comment))
		}
		buf.WriteString("\n")
	}
	buf.WriteString("}\n\n")

	// 生成 Update Request
	buf.WriteString(fmt.Sprintf("// Update%sReq 更新%s请求\n", entityName, table.Comment))
	buf.WriteString(fmt.Sprintf("type Update%sReq struct {\n", entityName))

	// 先添加 ID 字段（用于路径参数）
	for _, col := range table.Columns {
		if col.IsPrimaryKey {
			buf.WriteString(fmt.Sprintf("\t%s %s `json:\"%s\"`", parser.ToCamelCase(col.Name), col.GoType, col.JSONTag))
			if col.Comment != "" {
				buf.WriteString(fmt.Sprintf(" // %s", col.Comment))
			}
			buf.WriteString("\n")
			break
		}
	}

	// 再添加其他可更新字段
	for _, col := range table.Columns {
		if col.IsPrimaryKey || col.IsAutoIncr || col.Name == "created_at" || col.Name == "updated_at" {
			continue
		}
		buf.WriteString(fmt.Sprintf("\t%s %s", parser.ToCamelCase(col.Name), col.GoType))
		tags := fmt.Sprintf(" `json:\"%s\" validate:\"omitempty\"`", col.JSONTag)
		buf.WriteString(tags)
		if col.Comment != "" {
			buf.WriteString(fmt.Sprintf(" // %s", col.Comment))
		}
		buf.WriteString("\n")
	}
	buf.WriteString("}\n\n")

	// 生成 Response
	buf.WriteString(fmt.Sprintf("// %sResp %s响应\n", entityName, table.Comment))
	buf.WriteString(fmt.Sprintf("type %sResp struct {\n", entityName))
	for _, col := range table.Columns {
		if col.Name == "password" {
			continue
		}
		buf.WriteString(fmt.Sprintf("\t%s %s", parser.ToCamelCase(col.Name), col.GoType))
		tags := fmt.Sprintf(" `json:\"%s\"`", col.JSONTag)
		buf.WriteString(tags)
		if col.Comment != "" {
			buf.WriteString(fmt.Sprintf(" // %s", col.Comment))
		}
		buf.WriteString("\n")
	}
	buf.WriteString("}\n")

	return buf.String()
}

// GenerateHandlers 生成 Handler 代码
func (g *CodeGenerator) GenerateHandlers() map[string]string {
	files := make(map[string]string)

	for _, table := range g.tables {
		moduleName := toModuleName(table.Name)

		// 生成 CRUD handlers
		files[fmt.Sprintf("create_%s_handler.go", moduleName)] = g.generateCreateHandler(table, moduleName)
		files[fmt.Sprintf("get_%s_handler.go", moduleName)] = g.generateGetHandler(table, moduleName)
		files[fmt.Sprintf("update_%s_handler.go", moduleName)] = g.generateUpdateHandler(table, moduleName)
		files[fmt.Sprintf("delete_%s_handler.go", moduleName)] = g.generateDeleteHandler(table, moduleName)
	}

	return files
}

