// generateModelForTable 为单个表生成 Model
func (g *CodeGenerator) generateModelForTable(table parser.TableDef, moduleName string) string {
	entityName := parser.ToCamelCase(moduleName)

	// 获取主键字段
	var pkField parser.ColumnDef
	for _, col := range table.Columns {
		if col.IsPrimaryKey {
			pkField = col
			break
		}
	}

	tmpl := `package model

import (
	"context"
	"database/sql"
	"time"

	"scene-script/pkg/stores/sqlx"
)

var (
	{{.LowerEntityName}}FieldNames        = "{{.SelectFields}}"
	{{.LowerEntityName}}RowsExpectAutoSet = "{{.InsertFields}}"
)

type {{.EntityName}} struct {
{{range .Columns}}	{{.FieldName}} {{.GoType}} ` + "`db:\"{{.DBTag}}\" json:\"{{.JSONTag}}\"`" + `{{if .Comment}} // {{.Comment}}{{end}}
{{end}}}

type {{.EntityName}}Model interface {
	{{.LowerEntityName}}Model
	// BEGIN: customizable methods (preserved during code regeneration)
	// Add your custom methods here
	// END: customizable methods
}

type {{.LowerEntityName}}Model interface {
	Insert(ctx context.Context, data *{{.EntityName}}) (int64, error)
	FindOne(ctx context.Context, id {{.PKType}}) (*{{.EntityName}}, error)
	Update(ctx context.Context, data *{{.EntityName}}) error
	Delete(ctx context.Context, id {{.PKType}}) error
	Trans(ctx context.Context, fn func(context.Context, {{.EntityName}}Model) error) error
}

type default{{.EntityName}}Model struct {
	conn  sqlx.SqlConn
	table string
}

func New{{.EntityName}}Model(conn sqlx.SqlConn) {{.EntityName}}Model {
	return &default{{.EntityName}}Model{
		conn:  conn,
		table: "{{.TableName}}",
	}
}

func (m *default{{.EntityName}}Model) Insert(ctx context.Context, data *{{.EntityName}}) (int64, error) {
	query := ` + "`INSERT INTO `" + ` + m.table + ` + "` ({{.InsertFields}}) VALUES ({{.InsertPlaceholders}})`" + `
	
	result, err := m.conn.ExecCtx(ctx, query, {{.InsertValues}})
	if err != nil {
		return 0, err
	}
	
	return result.LastInsertId()
}

func (m *default{{.EntityName}}Model) FindOne(ctx context.Context, id {{.PKType}}) (*{{.EntityName}}, error) {
	query := ` + "`SELECT `" + ` + {{.LowerEntityName}}FieldNames + ` + "` FROM `" + ` + m.table + ` + "` WHERE {{.PKColumn}} = ?`" + `
	
	var data {{.EntityName}}
	err := m.conn.QueryRowCtx(ctx, &data, query, id)
	
	switch err {
	case nil:
		return &data, nil
	case sql.ErrNoRows:
		return nil, ErrNotFound
	default:
		return nil, err
	}
}

func (m *default{{.EntityName}}Model) Update(ctx context.Context, data *{{.EntityName}}) error {
	query := ` + "`UPDATE `" + ` + m.table + ` + "` SET {{.UpdateFields}} WHERE {{.PKColumn}} = ?`" + `
	
	_, err := m.conn.ExecCtx(ctx, query, {{.UpdateValues}})
	return err
}

func (m *default{{.EntityName}}Model) Delete(ctx context.Context, id {{.PKType}}) error {
	query := ` + "`DELETE FROM `" + ` + m.table + ` + "` WHERE {{.PKColumn}} = ?`" + `
	
	_, err := m.conn.ExecCtx(ctx, query, id)
	return err
}

func (m *default{{.EntityName}}Model) Trans(ctx context.Context, fn func(context.Context, {{.EntityName}}Model) error) error {
	return m.conn.TransactCtx(ctx, func(ctx context.Context, conn sqlx.SqlConn) error {
		transModel := &default{{.EntityName}}Model{
			conn:  conn,
			table: m.table,
		}
		return fn(ctx, transModel)
	})
}
`

	type ModelColumn struct {
		FieldName string
		GoType    string
		DBTag     string
		JSONTag   string
		Comment   string
	}

	var columns []ModelColumn
	for _, col := range table.Columns {
		columns = append(columns, ModelColumn{
			FieldName: parser.ToCamelCase(col.Name),
			GoType:    col.GoType,
			DBTag:     col.Name,
			JSONTag:   col.JSONTag,
			Comment:   col.Comment,
		})
	}

	// 构建字段列表
	var insertFields, insertPlaceholders, insertValues []string
	var updateFields, updateValues []string
	var selectFields []string

	for _, col := range table.Columns {
		selectFields = append(selectFields, col.Name)

		if col.IsPrimaryKey || col.IsAutoIncr || col.Name == "created_at" || col.Name == "updated_at" {
			continue
		}

		insertFields = append(insertFields, col.Name)
		insertPlaceholders = append(insertPlaceholders, "?")
		insertValues = append(insertValues, "data."+parser.ToCamelCase(col.Name))

		updateFields = append(updateFields, col.Name+" = ?")
		updateValues = append(updateValues, "data."+parser.ToCamelCase(col.Name))
	}

	// 添加时间字段
	insertFields = append(insertFields, "created_at", "updated_at")
	insertPlaceholders = append(insertPlaceholders, "?", "?")
	insertValues = append(insertValues, "time.Now()", "time.Now()")

	updateFields = append(updateFields, "updated_at = ?")
	updateValues = append(updateValues, "time.Now()")
	updateValues = append(updateValues, "data."+parser.ToCamelCase(pkField.Name))

	data := map[string]interface{}{
		"EntityName":         entityName,
		"LowerEntityName":    toLowerFirst(entityName),
		"TableName":          table.Name,
		"Columns":            columns,
		"PKColumn":           pkField.Name,
		"PKType":             pkField.GoType,
		"InsertFields":       strings.Join(insertFields, ", "),
		"InsertPlaceholders": strings.Join(insertPlaceholders, ", "),
		"InsertValues":       strings.Join(insertValues, ", "),
		"SelectFields":       strings.Join(selectFields, ", "),
		"UpdateFields":       strings.Join(updateFields, ", "),
		"UpdateValues":       strings.Join(updateValues, ", "),
	}

	t := template.Must(template.New("model").Parse(tmpl))
	var buf bytes.Buffer
	t.Execute(&buf, data)

	return buf.String()
}

// toModuleName 转换为模块名（单数形式，去除前缀）
func toModuleName(tableName string) string {
	if tableName == "" {
		return ""
	}

	name := tableName

	// 去除常见表前缀
	prefixes := []string{"sys_", "t_", "tb_", "tbl_"}
	for _, prefix := range prefixes {
		if strings.HasPrefix(name, prefix) {
			name = strings.TrimPrefix(name, prefix)
			break
		}
	}

	// 去除复数后缀 s（如果是下划线分隔的，只处理最后一部分）
	// sys_user -> user (已去除前缀)
	// users -> user
	if strings.HasSuffix(name, "s") && !strings.HasSuffix(name, "ss") {
		name = strings.TrimSuffix(name, "s")
	}

	return name
}

// toLowerFirst 将首字母转换为小写
func toLowerFirst(s string) string {
	if s == "" {
		return ""
	}
	return strings.ToLower(s[:1]) + s[1:]
}
